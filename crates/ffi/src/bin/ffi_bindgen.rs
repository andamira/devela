use std::{
    env,
    fmt::Write as _,
    fs,
    path::{Path, PathBuf},
};

type Result<T> = std::result::Result<T, Box<dyn std::error::Error>>;

const GENERATED: &str =
    "Generated by devela_ffi_bindgen from abi/devela_ffi.abi. Do not edit by hand.";

fn main() -> Result<()> {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let abi_path = root.join("abi/devela_ffi.abi");
    let input = fs::read_to_string(&abi_path)?;
    let funcs = parse_manifest(&input)?;
    write_file(&root.join("pkg/c/devela_ffi.h"), &gen_c_header(&funcs)?)?;
    write_file(&root.join("pkg/odin/common/gen_common.odin"), &gen_odin_common(&funcs)?)?;
    write_file(&root.join("pkg/odin/import/gen_import.odin"), &gen_odin_import(&funcs)?)?;
    write_file(&root.join("pkg/odin/load/gen_load.odin"), &gen_odin_load(&funcs)?)?;
    println!("generated bindings from {}", abi_path.display());
    Ok(())
}
fn write_file(path: &Path, text: &str) -> Result<()> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(path, text)?;
    println!("generated {}", path.display());
    Ok(())
}

/* ABI */

// These are part of the public C ABI. Values must not be changed casually.
// Add new negative errors for failures. Keep 0 as success.
// Positive statuses are non-error control results.
const STATUS_TYPE: &str = "devela_status";
const STATUS_CONSTANTS: &[StatusConst] = &[
    StatusConst::new("DEVELA_OK", 0, "Success."),
    StatusConst::new("DEVELA_NO_EVENT", 1, "No event was available."),
    StatusConst::new("DEVELA_ERR_NULL", -1, "A required pointer was null."),
    StatusConst::new("DEVELA_ERR_INVALID", -2, "An argument was invalid."),
    StatusConst::new("DEVELA_ERR_PANIC", -3, "A Rust panic was caught at the ABI boundary."),
    StatusConst::new("DEVELA_ERR_UNSUPPORTED", -4, "The requested operation is unsupported."),
];
struct StatusConst {
    name: &'static str,
    value: i32,
    #[expect(dead_code)]
    doc: &'static str,
}
impl StatusConst {
    const fn new(name: &'static str, value: i32, doc: &'static str) -> Self {
        Self { name, value, doc }
    }
}

#[derive(Clone, Debug)]
struct FnSpec {
    name: String,
    ret: AbiType,
    args: Vec<ArgSpec>,
}
#[derive(Clone, Debug)]
struct ArgSpec {
    name: String,
    ty: AbiType,
}
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
enum AbiType {
    Void,
    I32,
    U32,
    Usize,
    Status,
    CStr,
    ConstU8Ptr,
    UsizePtr,
}
fn parse_manifest(input: &str) -> Result<Vec<FnSpec>> {
    let mut funcs = Vec::new();
    for (index, raw_line) in input.lines().enumerate() {
        let line_no = index + 1;
        let line = raw_line.split('#').next().unwrap_or("").trim();
        if line.is_empty() {
            continue;
        }
        let mut parts = line.split_whitespace();
        let name = parts.next().ok_or_else(|| format!("line {line_no}: missing function name"))?;
        let ret = parts.next().ok_or_else(|| format!("line {line_no}: missing return type"))?;
        let args = parts.collect::<Vec<_>>().join(" ");
        if args.is_empty() {
            return Err(format!("line {line_no}: missing args").into());
        }
        funcs.push(FnSpec {
            name: name.to_string(),
            ret: parse_type(ret).map_err(|err| format!("line {line_no}: {err}"))?,
            args: parse_args(line_no, &args)?,
        });
    }
    if funcs.is_empty() {
        return Err("ABI manifest has no functions".into());
    }
    Ok(funcs)
}
fn parse_args(line_no: usize, input: &str) -> Result<Vec<ArgSpec>> {
    let input = input.trim();
    if input == "void" || input == "()" {
        return Ok(Vec::new());
    }
    let mut args = Vec::new();
    for raw_arg in input.split(',') {
        let raw_arg = raw_arg.trim();
        let (ty, name) = raw_arg.split_once(':').ok_or_else(|| {
            format!("line {line_no}: invalid arg `{raw_arg}`, expected type:name")
        })?;
        let ty = parse_type(ty.trim()).map_err(|err| format!("line {line_no}: {err}"))?;
        let name = name.trim();
        if name.is_empty() {
            return Err(format!("line {line_no}: empty arg name").into());
        }
        args.push(ArgSpec { name: name.to_string(), ty });
    }
    Ok(args)
}
fn parse_type(input: &str) -> std::result::Result<AbiType, String> {
    match input {
        "void" => Ok(AbiType::Void),
        "i32" => Ok(AbiType::I32),
        "u32" => Ok(AbiType::U32),
        "usize" => Ok(AbiType::Usize),
        "devela_status" | "status" => Ok(AbiType::Status),
        "cstr" => Ok(AbiType::CStr),
        "const_u8_ptr" => Ok(AbiType::ConstU8Ptr),
        "usize_ptr" => Ok(AbiType::UsizePtr),
        other => Err(format!("unknown ABI type `{other}`")),
    }
}

/* C */

const C_HEADER_PRELUDE: &str = r#"
#ifndef DEVELA_FFI_H
#define DEVELA_FFI_H

#include <stdint.h>
#include <stddef.h>

#ifdef __cplusplus
extern "C" {
#endif

"#;
const C_HEADER_END: &str = r#"
#ifdef __cplusplus
}
#endif

#endif
"#;
fn gen_c_header(funcs: &[FnSpec]) -> Result<String> {
    let mut out = String::new();
    writeln!(out, "// {GENERATED}")?;
    out.push_str(C_HEADER_PRELUDE);
    push_c_status_constants(&mut out)?;
    for func in funcs {
        writeln!(out, "{} {}({});", c_type(func.ret), func.name, c_args(&func.args))?;
    }
    out.push_str(C_HEADER_END);
    Ok(out)
}
fn c_type(ty: AbiType) -> &'static str {
    match ty {
        AbiType::Void => "void",
        AbiType::I32 => "int32_t",
        AbiType::U32 => "uint32_t",
        AbiType::Usize => "size_t",
        AbiType::Status => "devela_status",
        AbiType::CStr => "const char *",
        AbiType::ConstU8Ptr => "const uint8_t *",
        AbiType::UsizePtr => "size_t *",
    }
}
fn c_args(args: &[ArgSpec]) -> String {
    if args.is_empty() {
        return "void".to_string();
    }
    args.iter()
        .map(|arg| format!("{} {}", c_type(arg.ty), arg.name))
        .collect::<Vec<_>>()
        .join(", ")
}
fn push_c_status_constants(out: &mut String) -> Result<()> {
    writeln!(out, "typedef int32_t {STATUS_TYPE};")?;
    writeln!(out)?;
    writeln!(out, "enum {{")?;
    for (i, c) in STATUS_CONSTANTS.iter().enumerate() {
        if i == 2 {
            writeln!(out)?;
        }
        let comma = if i + 1 == STATUS_CONSTANTS.len() { "" } else { "," };
        writeln!(out, "    {} = {}{}", c.name, c.value, comma)?;
    }
    writeln!(out, "}};")?;
    writeln!(out)?;
    Ok(())
}

/* Odin */

const ODIN_COMMON_PRELUDE: &str = r#"// Shared C ABI declarations.

package devela_ffi_common

"#;
fn gen_odin_common(funcs: &[FnSpec]) -> Result<String> {
    let mut out = String::new();
    writeln!(out, "// {GENERATED}")?;
    out.push_str(ODIN_COMMON_PRELUDE);
    push_odin_status_constants(&mut out)?;
    for func in funcs {
        writeln!(out, "{} :: {}", odin_proc_alias(func), odin_proc_type(func, false))?;
    }
    Ok(out)
}

const ODIN_IMPORT_PRELUDE: &str = r#"// Compile/link-time foreign import.

package devela_ffi_import

import common "../common"

Devela_Status :: common.Devela_Status

DEVELA_OK              :: common.DEVELA_OK
DEVELA_NO_EVENT        :: common.DEVELA_NO_EVENT
DEVELA_ERR_NULL        :: common.DEVELA_ERR_NULL
DEVELA_ERR_INVALID     :: common.DEVELA_ERR_INVALID
DEVELA_ERR_PANIC       :: common.DEVELA_ERR_PANIC
DEVELA_ERR_UNSUPPORTED :: common.DEVELA_ERR_UNSUPPORTED

DEVELA_FFI_STATIC :: false

when ODIN_OS == .Linux {
    when DEVELA_FFI_STATIC {
        foreign import devela_ffi "../../../libs/libdevela_ffi.a"
    } else {
        foreign import devela_ffi "../../../libs/libdevela_ffi.so"
    }
} else when ODIN_OS == .Darwin {
    when DEVELA_FFI_STATIC {
        foreign import devela_ffi "../../../libs/libdevela_ffi.a"
    } else {
        foreign import devela_ffi "../../../libs/libdevela_ffi.dylib"
    }
} else when ODIN_OS == .Windows {
    foreign import devela_ffi "../../../libs/devela_ffi.lib"
}

foreign devela_ffi {
"#;
fn gen_odin_import(funcs: &[FnSpec]) -> Result<String> {
    let mut out = String::new();
    writeln!(out, "// {GENERATED}")?;
    out.push_str(ODIN_IMPORT_PRELUDE);
    for func in funcs {
        writeln!(out, "    {} :: {} ---", func.name, odin_proc_type(func, true))?;
    }
    out.push_str("}\n");
    Ok(out)
}

const ODIN_LOAD_PRELUDE: &str = r#"// Runtime symbol binding.

package devela_ffi_load

import "core:dynlib"
import common "../common"

Library :: struct {
    handle: dynlib.Library,

"#;
fn gen_odin_load(funcs: &[FnSpec]) -> Result<String> {
    let mut out = String::new();
    writeln!(out, "// {GENERATED}")?;
    out.push_str(ODIN_LOAD_PRELUDE);
    for func in funcs {
        writeln!(out, "    {}: {},", odin_field_name(&func.name), odin_proc_alias_qualified(func))?;
    }
    writeln!(out, "}}")?;
    writeln!(out)?;
    writeln!(out, "load_fns :: proc(handle: dynlib.Library) -> (lib: Library, ok: bool) {{")?;
    for (index, func) in funcs.iter().enumerate() {
        let field = odin_field_name(&func.name);
        writeln!(
            out,
            "    raw_{field}, ok{} := dynlib.symbol_address(handle, \"{}\")",
            index + 1,
            func.name,
        )?;
    }
    writeln!(out)?;
    write!(out, "    if !(")?;
    for index in 0..funcs.len() {
        if index != 0 {
            write!(out, " && ")?;
        }
        write!(out, "ok{}", index + 1)?;
    }
    writeln!(out, ") {{")?;
    writeln!(out, "        return {{}}, false")?;
    writeln!(out, "    }}")?;
    writeln!(out)?;
    writeln!(out, "    lib = Library {{")?;
    writeln!(out, "        handle = handle,")?;
    for func in funcs {
        let field = odin_field_name(&func.name);
        writeln!(out, "        {field} = cast({})raw_{field},", odin_proc_alias_qualified(func),)?;
    }
    writeln!(out, "    }}")?;
    writeln!(out)?;
    writeln!(out, "    return lib, true")?;
    writeln!(out, "}}")?;
    Ok(out)
}
fn odin_type(ty: AbiType) -> &'static str {
    match ty {
        AbiType::Void => "",
        AbiType::I32 => "i32",
        AbiType::U32 => "u32",
        AbiType::Usize => "uint",
        AbiType::Status => "Devela_Status",
        AbiType::CStr => "cstring",
        AbiType::ConstU8Ptr => "^u8",
        AbiType::UsizePtr => "^uint",
    }
}
fn odin_args(args: &[ArgSpec]) -> String {
    args.iter()
        .map(|arg| format!("{}: {}", arg.name, odin_type(arg.ty)))
        .collect::<Vec<_>>()
        .join(", ")
}
fn odin_proc_type(func: &FnSpec, foreign_decl: bool) -> String {
    let mut out = String::new();
    if foreign_decl {
        out.push_str("proc \"c\"");
    } else {
        out.push_str("proc \"c\"");
    }
    out.push_str(" (");
    out.push_str(&odin_args(&func.args));
    out.push(')');
    if func.ret != AbiType::Void {
        out.push_str(" -> ");
        out.push_str(odin_type(func.ret));
    }
    out
}
fn odin_proc_alias(func: &FnSpec) -> String {
    let stem = func.name.strip_prefix("devela_").unwrap_or(&func.name);
    let mut out = String::new();
    for (index, part) in stem.split('_').enumerate() {
        if index != 0 {
            out.push('_');
        }
        let mut chars = part.chars();
        if let Some(first) = chars.next() {
            out.extend(first.to_uppercase());
            out.push_str(chars.as_str());
        }
    }
    out.push_str("_Proc");
    out
}
fn odin_proc_alias_qualified(func: &FnSpec) -> String {
    format!("common.{}", odin_proc_alias(func))
}
fn odin_field_name(name: &str) -> String {
    name.strip_prefix("devela_").unwrap_or(name).to_string()
}
fn push_odin_status_constants(out: &mut String) -> Result<()> {
    writeln!(out, "Devela_Status :: i32")?;
    writeln!(out)?;
    for c in STATUS_CONSTANTS {
        writeln!(out, "{} :: Devela_Status({})", c.name, c.value)?;
    }
    writeln!(out)?;
    Ok(())
}
