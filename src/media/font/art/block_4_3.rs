#[test]
fn test_font() {
    use {crate::CharIter, FONT_ART_BLOCK_4_3 as FONT};

    let len = FONT.len();
    let [mut i, mut j] = [0, 0];

    while i < len {
        #[cfg(feature = "std")]
        crate::cdbg![1# FONT[i]]; // show the symbol we're testing
        while j < 3 {
            let row = FONT[i][j];
            let iter = CharIter::<&str>::new(row);
            assert_eq![iter.count(), 4];
            j += 1;
        }
        j = 0;
        i += 1;
    }
}
